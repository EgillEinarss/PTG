	private String[] Tree2Str(TreeSet<String> html, TreeSet<String> tex, String sepHtml, String sepTex){
		String[] o = new String[2];
		o[0] = "";
		o[1] = "";
		for(String a : html){
			if(!o[0].equals("")) o[0] += sepHtml;
			o[0] += a;
		}
		for(String a : tex){
			if(!o[1].equals("")) o[1] += sepTex;
			o[1] += a;
		}
		return o;
	}
	
